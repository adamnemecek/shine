/// The RegularCellData structure holds information about the triangulation
/// used for a single equivalence class in the modified Marching Cubes algorithm,
/// described in Section 3.2.
pub struct RegularCellData {
    pub counts: u8,
    pub indices: [u8; 15],
}

impl RegularCellData {
    const fn new(counts: u8, indices: [u8; 15]) -> RegularCellData {
        RegularCellData { counts, indices }
    }

    pub fn get_vertex_count(&self) -> usize {
        (self.counts >> 4) as usize
    }

    pub fn get_triangle_count(&self) -> usize {
        (self.counts & 0x0f) as usize
    }
}

/// The TransitionCellData structure holds information about the triangulation
/// used for a single equivalence class in the Transvoxel Algorithm transition cell,
/// described in Section 4.3.
pub struct TransitionCellData {
    pub counts: u8,
    pub indices: [u8; 36],
}

impl TransitionCellData {
    const fn new(counts: u8, indices: [u8; 36]) -> TransitionCellData {
        TransitionCellData { counts, indices }
    }

    pub fn get_vertex_count(&self) -> usize {
        (self.counts >> 4) as usize
    }

    pub fn get_triangle_count(&self) -> usize {
        (self.counts & 0x0f) as usize
    }
}

